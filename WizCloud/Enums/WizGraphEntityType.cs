namespace WizCloud;
/// <summary>
/// Represents the graph entity types returned by the Wiz API.
/// </summary>
public enum WizGraphEntityType {
    /// <summary>
    /// A user entity.
    /// </summary>
    USER,

    /// <summary>
    /// A service account entity.
    /// </summary>
    SERVICE_ACCOUNT,

    /// <summary>
    /// A group entity.
    /// </summary>
    GROUP,

    /// <summary>
    /// An access key entity.
    /// </summary>
    ACCESS_KEY
}